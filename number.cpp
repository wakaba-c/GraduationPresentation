#include "number.h"
#include "manager.h"
#include "renderer.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CNumber::CNumber(CScene::PRIORITY obj = CScene::PRIORITY_UI) : CScene2D(obj)
{

}

//=============================================================================
// デストラクタ
//=============================================================================
CNumber::~CNumber()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CNumber::Init(void)
{
	// 頂点情報の作成
	BindTexture("data/tex/number001.png");
	CScene2D::Init();
	return S_OK;
}

//=============================================================================
// 開放処理
//=============================================================================
void CNumber::Uninit(void)
{
	CScene2D::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CNumber::Update(void)
{
	CScene2D::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CNumber::Draw(void)
{
	CScene2D::Draw();
}

//=============================================================================
// クリエイト処理
//=============================================================================
CNumber *CNumber::Create(void)
{
	CNumber *pNumber;
	pNumber = new CNumber(PRIORITY_UI);

	if (pNumber != NULL)
	{// 数字が存在していたとき
		pNumber->Init();			// 初期化処理
	}
	return pNumber;
}

//=============================================================================
// ロード処理
//=============================================================================
HRESULT CNumber::Load(void)
{
	// テクスチャの取得
	CManager::Load("data/tex/number.png");
	return S_OK;
}